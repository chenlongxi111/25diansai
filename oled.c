void OLED_Init(void) {
    for(volatile int i=0;i<800000;i++) __asm("nop");
    uint8_t c[]={0xAE,0xD5,0x80,0xA8,0x1F,0xD3,0x00,0x40,0x8D,0x14,
                 0x20,0x00,0xA1,0xC8,0xDA,0x02,0x81,0xCF,0xD9,0xF1,
                 0xDB,0x40,0xA4,0xA6,0xAF};
    for(int i=0;i<25;i++){i2c_s();i2c_b(0x78);i2c_b(0x00);i2c_b(c[i]);i2c_p();}
}
void OLED_Clear(void) {
    for(int p=0;p<4;p++){i2c_s();i2c_b(0x78);i2c_b(0x00);i2c_b(0xB0+p);i2c_b(0x00);i2c_b(0x10);i2c_p();
        for(int i=0;i<128;i++){i2c_s();i2c_b(0x78);i2c_b(0x40);i2c_b(0x00);i2c_p();}
    }
}
void OLED_ShowStr(uint8_t x, uint8_t y, const char *s) {
    while(*s) {
        uint8_t c=*s++; if(c<' '||c>'~')c=' ';
        const uint8_t *f=font6x8[c-32];
        for(int pg=0;pg<2;pg++){ /* 每个字符占2行(16px高) */
            i2c_s();i2c_b(0x78);i2c_b(0x00);
            i2c_b(0xB0+y+pg);i2c_b(0x00+((x&0xF0)>>4));i2c_b(0x10+(x&0x0F));
            i2c_p();
            i2c_s();i2c_b(0x78);i2c_b(0x40);
            for(int i=0;i<6;i++){i2c_b(pg?f[i]:f[i]);i2c_b(pg?f[i]:f[i]);}
            i2c_p();
        }
        x+=12;
    }
}
void OLED_ShowNum(uint8_t x, uint8_t y, int n) {
    char b[4]; b[0]='0'+n%10; b[1]=0;
    if(n>=10){b[1]=b[0];b[0]='0'+(n/10)%10;b[2]=0;}
    if(n>=100){b[2]=b[1];b[1]=b[0];b[0]='0'+(n/100)%10;b[3]=0;}
    OLED_ShowStr(x,y,b);
}
